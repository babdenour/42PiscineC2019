#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	int i;

	i = 0;
	if (nb == -2147483648)
		write(1, "-2147483648", 11);
	if ((nb <= 2147483647) && (nb >= -2147483648))
	{
		if (nb < 0)
		{
			nb = -nb;
			write(1, "-", 1);
		}
		if (nb >= 10)
		{
			ft_putnbr(nb / 10);
			ft_putnbr(nb % 10);
		}
		else
			print(nb + '0');
	}
}
